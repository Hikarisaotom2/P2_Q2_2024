    private void jcb_estiloLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcb_estiloLetraActionPerformed
         String nombreFuente = txt_tipoLetra.getText();
         String estilo = jcb_estiloLetra.getSelectedItem().toString();
         Font fuenteVieja= txt_contenido.getFont();
         String fuente = fuenteVieja.getFontName();
         int size = fuenteVieja.getSize();
        Font nuevaFuente ;
        if (estilo.equalsIgnoreCase("Negrita")) {
            nuevaFuente= new Font(fuente,Font.BOLD,size);
        } else if (estilo.equalsIgnoreCase("Italica")) {
             nuevaFuente= new Font(fuente,Font.ITALIC,size);
        } else {
         nuevaFuente= new Font(fuente,Font.PLAIN,size);
        }
        txt_contenido.setFont(nuevaFuente);
    }//GEN-LAST:event_jcb_estiloLetraActionPerformed

